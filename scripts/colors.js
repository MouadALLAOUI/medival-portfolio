export default function getColorForTag(tag) {
  const normalized = tag.toLowerCase();

  const match = tagcolor.find(entry =>
    entry.tags.some(t => t.toLowerCase() === normalized)
  );

  return match ? colors[match.color] : colors[Math.floor(Math.random() * colors.length)];
}